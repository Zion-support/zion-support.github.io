#!/usr/bin/env node

const { execSync, spawn } = require('child_process');
const fs = require('fs');
const path = require('path');
const chokidar = require('chokidar');

class DevelopmentWorkflowAutomation {
  constructor() {
    this.projectRoot = process.cwd();
    this.logFile = path.join(this.projectRoot, 'automation/logs/dev-workflow.log');
    this.config = this.loadConfig();
    this.fileWatchers = new Map();
    this.gitHooks = new Map();
    this.ensureLogDirectory();
    this.setupGitHooks();
  }

  ensureLogDirectory() {
    const logDir = path.dirname(this.logFile);
    if (!fs.existsSync(logDir)) {
      fs.mkdirSync(logDir, { recursive: true });
    }
  }

  loadConfig() {
    const configPath = path.join(this.projectRoot, 'automation/config/dev-workflow.json');
    if (fs.existsSync(configPath)) {
      try {
        return JSON.parse(fs.readFileSync(configPath, 'utf8'));
      } catch (error) {
        console.warn('Failed to load config, using defaults');
      }
    }
    
    return {
      watchPatterns: [
        'src/**/*.{js,jsx,ts,tsx}',
        'server/**/*.{js,ts}',
        'components/**/*.{js,jsx,ts,tsx}',
        'pages/**/*.{js,jsx,ts,tsx}'
      ],
      ignorePatterns: [
        '**/node_modules/**',
        '**/dist/**',
        '**/build/**',
        '**/*.log',
        '**/coverage/**'
      ],
      autoTasks: {
        lint: true,
        typeCheck: true,
        test: false,
        build: false,
        format: true
      },
      gitHooks: {
        preCommit: true,
        prePush: true,
        postMerge: true
      }
    };
  }

  log(message, level = 'info') {
    const timestamp = new Date().toISOString();
    const logMessage = `[${timestamp}] [${level.toUpperCase()}] ${message}\n`;
    console.log(`[${level.toUpperCase()}] ${message}`);
    fs.appendFileSync(this.logFile, logMessage);
  }

  async executeCommand(command, options = {}) {
    try {
      const result = execSync(command, { 
        encoding: 'utf8', 
        stdio: 'pipe',
        cwd: this.projectRoot,
        ...options 
      });
      return { success: true, output: result };
    } catch (error) {
      return { success: false, error: error.message };
    }
  }

  setupGitHooks() {
    this.log('🔧 Setting up Git hooks...');
    
    const hooksDir = path.join(this.projectRoot, '.git/hooks');
    if (!fs.existsSync(hooksDir)) {
      this.log('❌ Git hooks directory not found', 'warn');
      return;
    }

    // Pre-commit hook
    if (this.config.gitHooks.preCommit) {
      this.createGitHook('pre-commit', this.preCommitHook.bind(this));
    }

    // Pre-push hook
    if (this.config.gitHooks.prePush) {
      this.createGitHook('pre-push', this.prePushHook.bind(this));
    }

    // Post-merge hook
    if (this.config.gitHooks.postMerge) {
      this.createGitHook('post-merge', this.postMergeHook.bind(this));
    }

    this.log('✅ Git hooks configured');
  }

  createGitHook(hookName, hookFunction) {
    const hookPath = path.join(this.projectRoot, '.git/hooks', hookName);
    const hookContent = `#!/bin/bash
# Auto-generated by Development Workflow Automation
cd "${this.projectRoot}"
node scripts/dev-workflow-automation.js git-hook ${hookName}
`;

    try {
      fs.writeFileSync(hookPath, hookContent);
      fs.chmodSync(hookPath, '755');
      this.gitHooks.set(hookName, hookFunction);
      this.log(`✅ Created Git hook: ${hookName}`);
    } catch (error) {
      this.log(`❌ Failed to create Git hook ${hookName}: ${error.message}`, 'error');
    }
  }

  async preCommitHook() {
    this.log('🔍 Running pre-commit checks...');
    
    const checks = [];
    
    if (this.config.autoTasks.lint) {
      checks.push(this.runLintCheck());
    }
    
    if (this.config.autoTasks.typeCheck) {
      checks.push(this.runTypeCheck());
    }
    
    if (this.config.autoTasks.format) {
      checks.push(this.runFormatCheck());
    }
    
    const results = await Promise.all(checks);
    const failedChecks = results.filter(result => !result.success);
    
    if (failedChecks.length > 0) {
      this.log('❌ Pre-commit checks failed', 'error');
      failedChecks.forEach(check => {
        this.log(`  - ${check.name}: ${check.error}`, 'error');
      });
      process.exit(1);
    }
    
    this.log('✅ Pre-commit checks passed');
  }

  async prePushHook() {
    this.log('🧪 Running pre-push tests...');
    
    if (this.config.autoTasks.test) {
      const testResult = await this.runTests();
      if (!testResult.success) {
        this.log('❌ Tests failed, push blocked', 'error');
        process.exit(1);
      }
    }
    
    this.log('✅ Pre-push checks passed');
  }

  async postMergeHook() {
    this.log('📦 Running post-merge tasks...');
    
    // Install dependencies if package files changed
    const packageFiles = ['package.json', 'package-lock.json', 'yarn.lock'];
    const changedFiles = await this.getChangedFiles();
    
    if (packageFiles.some(file => changedFiles.includes(file))) {
      this.log('📦 Package files changed, installing dependencies...');
      await this.installDependencies();
    }
    
    // Restart PM2 processes if needed
    await this.restartPM2Processes();
    
    this.log('✅ Post-merge tasks completed');
  }

  async getChangedFiles() {
    try {
      const result = execSync('git diff --name-only HEAD~1', { 
        encoding: 'utf8', 
        cwd: this.projectRoot 
      });
      return result.trim().split('\n').filter(Boolean);
    } catch (error) {
      return [];
    }
  }

  async runLintCheck() {
    this.log('🔍 Running lint check...');
    
    try {
      const result = await this.executeCommand('npm run lint');
      if (result.success) {
        this.log('✅ Lint check passed');
        return { name: 'Lint', success: true };
      } else {
        this.log('❌ Lint check failed', 'error');
        return { name: 'Lint', success: false, error: result.error };
      }
    } catch (error) {
      return { name: 'Lint', success: false, error: error.message };
    }
  }

  async runTypeCheck() {
    this.log('🔍 Running type check...');
    
    try {
      const result = await this.executeCommand('npm run type-check');
      if (result.success) {
        this.log('✅ Type check passed');
        return { name: 'Type Check', success: true };
      } else {
        this.log('❌ Type check failed', 'error');
        return { name: 'Type Check', success: false, error: result.error };
      }
    } catch (error) {
      return { name: 'Type Check', success: false, error: error.message };
    }
  }

  async runFormatCheck() {
    this.log('🔍 Running format check...');
    
    try {
      // Check if files are properly formatted
      const result = await this.executeCommand('npx prettier --check "src/**/*.{js,jsx,ts,tsx}"');
      if (result.success) {
        this.log('✅ Format check passed');
        return { name: 'Format', success: true };
      } else {
        this.log('❌ Format check failed', 'error');
        return { name: 'Format', success: false, error: result.error };
      }
    } catch (error) {
      return { name: 'Format', success: false, error: error.message };
    }
  }

  async runTests() {
    this.log('🧪 Running tests...');
    
    try {
      const result = await this.executeCommand('npm test');
      if (result.success) {
        this.log('✅ Tests passed');
        return { success: true };
      } else {
        this.log('❌ Tests failed', 'error');
        return { success: false, error: result.error };
      }
    } catch (error) {
      return { success: false, error: error.message };
    }
  }

  async installDependencies() {
    this.log('📦 Installing dependencies...');
    
    try {
      const result = await this.executeCommand('npm install');
      if (result.success) {
        this.log('✅ Dependencies installed');
        return true;
      } else {
        this.log('❌ Failed to install dependencies', 'error');
        return false;
      }
    } catch (error) {
      this.log(`❌ Installation error: ${error.message}`, 'error');
      return false;
    }
  }

  async restartPM2Processes() {
    this.log('🔄 Restarting PM2 processes...');
    
    try {
      const result = await this.executeCommand('pm2 restart all');
      if (result.success) {
        this.log('✅ PM2 processes restarted');
        return true;
      } else {
        this.log('❌ Failed to restart PM2 processes', 'error');
        return false;
      }
    } catch (error) {
      this.log(`❌ PM2 restart error: ${error.message}`, 'error');
      return false;
    }
  }

  startFileWatching() {
    this.log('👀 Starting file watching...');
    
    const watcher = chokidar.watch(this.config.watchPatterns, {
      ignored: this.config.ignorePatterns,
      persistent: true,
      ignoreInitial: true
    });

    watcher
      .on('add', (filePath) => this.handleFileChange('add', filePath))
      .on('change', (filePath) => this.handleFileChange('change', filePath))
      .on('unlink', (filePath) => this.handleFileChange('unlink', filePath))
      .on('error', (error) => this.log(`File watcher error: ${error.message}`, 'error'));

    this.fileWatchers.set('main', watcher);
    this.log('✅ File watching started');
  }

  async handleFileChange(event, filePath) {
    const relativePath = path.relative(this.projectRoot, filePath);
    this.log(`📁 File ${event}: ${relativePath}`);
    
    // Debounce rapid changes
    clearTimeout(this.fileChangeTimeout);
    this.fileChangeTimeout = setTimeout(async () => {
      await this.processFileChanges();
    }, 1000);
  }

  async processFileChanges() {
    this.log('🔄 Processing file changes...');
    
    if (this.config.autoTasks.lint) {
      await this.runLintCheck();
    }
    
    if (this.config.autoTasks.typeCheck) {
      await this.runTypeCheck();
    }
    
    // Auto-fix if possible
    if (this.config.autoTasks.format) {
      await this.autoFormatFiles();
    }
  }

  async autoFormatFiles() {
    this.log('🎨 Auto-formatting files...');
    
    try {
      const result = await this.executeCommand('npx prettier --write "src/**/*.{js,jsx,ts,tsx}"');
      if (result.success) {
        this.log('✅ Files auto-formatted');
      } else {
        this.log('❌ Auto-format failed', 'error');
      }
    } catch (error) {
      this.log(`❌ Auto-format error: ${error.message}`, 'error');
    }
  }

  async runDevelopmentTasks() {
    this.log('🚀 Running development tasks...');
    
    const tasks = [
      { name: 'Install Dependencies', fn: this.installDependencies.bind(this) },
      { name: 'Lint Check', fn: this.runLintCheck.bind(this) },
      { name: 'Type Check', fn: this.runTypeCheck.bind(this) },
      { name: 'Format Check', fn: this.runFormatCheck.bind(this) }
    ];
    
    for (const task of tasks) {
      this.log(`🔄 Running: ${task.name}`);
      await task.fn();
      await this.sleep(1000); // Small delay between tasks
    }
    
    this.log('✅ Development tasks completed');
  }

  async createDevelopmentScripts() {
    this.log('📝 Creating development scripts...');
    
    const scripts = {
      'dev:setup': 'node scripts/dev-workflow-automation.js setup',
      'dev:watch': 'node scripts/dev-workflow-automation.js watch',
      'dev:tasks': 'node scripts/dev-workflow-automation.js tasks',
      'dev:clean': 'node scripts/dev-workflow-automation.js clean'
    };
    
    const packagePath = path.join(this.projectRoot, 'package.json');
    if (fs.existsSync(packagePath)) {
      try {
        const packageJson = JSON.parse(fs.readFileSync(packagePath, 'utf8'));
        packageJson.scripts = { ...packageJson.scripts, ...scripts };
        fs.writeFileSync(packagePath, JSON.stringify(packageJson, null, 2));
        this.log('✅ Development scripts added to package.json');
      } catch (error) {
        this.log(`❌ Failed to update package.json: ${error.message}`, 'error');
      }
    }
  }

  async cleanProject() {
    this.log('🧹 Cleaning project...');
    
    const cleanTasks = [
      'rm -rf node_modules',
      'rm -rf dist',
      'rm -rf build',
      'rm -rf coverage',
      'rm -rf .nyc_output',
      'rm -rf automation/logs/*.log'
    ];
    
    for (const task of cleanTasks) {
      try {
        await this.executeCommand(task);
      } catch (error) {
        // Ignore errors for non-existent directories
      }
    }
    
    this.log('✅ Project cleaned');
  }

  sleep(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
  }

  async stop() {
    this.log('🛑 Stopping development workflow automation...');
    
    // Stop file watchers
    for (const [name, watcher] of this.fileWatchers) {
      watcher.close();
      this.log(`✅ Stopped file watcher: ${name}`);
    }
    
    this.fileWatchers.clear();
    this.log('✅ Development workflow automation stopped');
  }
}

// CLI handling
const automation = new DevelopmentWorkflowAutomation();
const command = process.argv[2];

async function main() {
  try {
    switch (command) {
      case 'setup':
        await automation.createDevelopmentScripts();
        break;
      case 'watch':
        automation.startFileWatching();
        // Keep the process running
        process.stdin.resume();
        break;
      case 'tasks':
        await automation.runDevelopmentTasks();
        break;
      case 'clean':
        await automation.cleanProject();
        break;
      case 'git-hook':
        const hookName = process.argv[3];
        const hookFunction = automation.gitHooks.get(hookName);
        if (hookFunction) {
          await hookFunction();
        } else {
          console.log(`Unknown git hook: ${hookName}`);
          process.exit(1);
        }
        break;
      default:
        console.log('Usage: node dev-workflow-automation.js [command]');
        console.log('\nCommands:');
        console.log('  setup      - Setup development environment');
        console.log('  watch      - Start file watching');
        console.log('  tasks      - Run development tasks');
        console.log('  clean      - Clean project');
        console.log('  git-hook   - Run git hook (internal use)');
        process.exit(1);
    }
  } catch (error) {
    automation.log(`❌ Error: ${error.message}`, 'error');
    process.exit(1);
  }
}

// Graceful shutdown
process.on('SIGINT', async () => {
  console.log('\n🛑 Shutting down development workflow automation...');
  await automation.stop();
  process.exit(0);
});

process.on('SIGTERM', async () => {
  console.log('\n🛑 Shutting down development workflow automation...');
  await automation.stop();
  process.exit(0);
});

main();