const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

class ComprehensiveSyntaxFixer {
  constructor() {
    this.projectRoot = process.cwd();
    this.fixedFiles = [];
    this.errors = [];
  }

  log(message, level = 'INFO') {
    const timestamp = new Date().toISOString();
    console.log(`[${timestamp}] [${level}] ${message}`);
  }

  findJavaScriptFiles() {
    const files = [];
    const searchDirectories = ['automation', 'scripts', 'app', 'pages', 'components', 'src'];
    
    searchDirectories.forEach(dir => {
      const dirPath = path.join(this.projectRoot, dir);
      if (fs.existsSync(dirPath)) {
        this.findJSFilesInDirectory(dirPath, files);
      }
    });

    return files;
  }

  findJSFilesInDirectory(dir, files) {
    const items = fs.readdirSync(dir);
    
    items.forEach(item => {
      const fullPath = path.join(dir, item);
      const stat = fs.statSync(fullPath);
      
      if (stat.isDirectory()) {
        this.findJSFilesInDirectory(fullPath, files);
      } else if (item.endsWith('.js') || item.endsWith('.cjs') || item.endsWith('.ts') || item.endsWith('.tsx')) {
        files.push(fullPath);
      }
    });
  }

  fixSyntaxErrors(filePath) {
    try {
      let content = fs.readFileSync(filePath, 'utf8');
      let originalContent = content;
      
      // Fix common syntax errors
      content = this.fixCommonSyntaxErrors(content);
      
      // Fix specific patterns
      content = this.fixSpecificPatterns(content);
      
      // Clean up the file
      content = this.cleanupFile(content);
      
      if (content !== originalContent) {
        fs.writeFileSync(filePath, content);
        this.fixedFiles.push(filePath);
        this.log(`✅ Fixed syntax errors in: ${path.relative(this.projectRoot, filePath)}`);
        return true;
      }
      
      return false;
    } catch (error) {
      this.errors.push({ file: filePath, error: error.message });
      this.log(`❌ Error fixing ${path.relative(this.projectRoot, filePath)}: ${error.message}`, 'ERROR');
      return false;
    }
  }

  fixCommonSyntaxErrors(content) {
    // Remove shebang lines that are causing issues
    content = content.replace(/^#!/gm, '//');
    
    // Fix duplicate shebang lines
    content = content.replace(/^\/\/.*\n\/\/.*$/gm, '//');
    
    // Fix class syntax issues
    content = content.replace(/class\s+\w+\s*\{\s*\/\/.*\n\}/g, (match) => {
      const className = match.match(/class\s+(\w+)/)?.[1];
      if (className) {
        return `class ${className} {\n  constructor() {\n    // TODO: Implement\n  }\n}`;
      }
      return match;
    });
    
    // Fix missing class declarations
    content = content.replace(/^\s*constructor\(\)\s*\{/gm, 'class AutoGeneratedClass {\n  constructor() {');
    
    // Fix orphaned closing braces
    content = content.replace(/^\s*\}\s*$/gm, '');
    
    // Fix duplicate require statements
    const requires = new Set();
    content = content.replace(/const\s+(\w+)\s*=\s*require\([^)]+\)/g, (match, varName) => {
      if (requires.has(varName)) {
        return '';
      }
      requires.add(varName);
      return match;
    });
    
    // Fix object syntax issues
    content = content.replace(/\{\s*\}\s*,/g, '');
    content = content.replace(/,\s*\}\s*$/g, '\n}');
    
    // Fix function syntax
    content = content.replace(/^\s*(\w+)\s*\(\s*\)\s*\{/gm, '  $1() {');
    
    // Fix string syntax
    content = content.replace(/'([^']*)':\s*'([^']*)'/g, '"$1": "$2"');
    
    // Fix semicolon issues
    content = content.replace(/;\s*;/g, ';');
    content = content.replace(/;\s*$/gm, '');
    
    return content;
  }

  fixSpecificPatterns(content) {
    // Fix backup file references
    content = content.replace(/^:backup-problematic-files\/.*$/gm, '');
    
    // Fix malformed object literals
    content = content.replace(/\{\s*'([^']*)':\s*'([^']*)'\s*\}/g, '{\n  "$1": "$2"\n}');
    
    // Fix malformed function calls
    content = content.replace(/^\s*(\w+)\s*\(\s*\)\s*\{/gm, '  $1() {');
    
    // Fix malformed class methods
    content = content.replace(/^\s*(\w+)\s*\(\s*[^)]*\s*\)\s*\{/gm, '  $1($2) {');
    
    return content;
  }

  cleanupFile(content) {
    // Remove empty lines at the beginning
    content = content.replace(/^\s*\n+/, '');
    
    // Remove multiple consecutive empty lines
    content = content.replace(/\n\s*\n\s*\n+/g, '\n\n');
    
    // Ensure proper line endings
    content = content.replace(/\r\n/g, '\n');
    
    // Remove trailing whitespace
    content = content.replace(/[ \t]+$/gm, '');
    
    // Ensure file ends with newline
    if (!content.endsWith('\n')) {
      content += '\n';
    }
    
    return content;
  }

  async fixAllFiles() {
    this.log('🔍 Finding JavaScript files with syntax errors...');
    
    const files = this.findJavaScriptFiles();
    this.log(`Found ${files.length} JavaScript files to check`);
    
    let fixedCount = 0;
    for (const file of files) {
      if (this.fixSyntaxErrors(file)) {
        fixedCount++;
      }
    }

    this.log(`\n📊 Syntax Fix Summary:`);
    this.log(`✅ Successfully fixed: ${fixedCount} files`);
    this.log(`❌ Failed to fix: ${this.errors.length} files`);
    
    if (this.errors.length > 0) {
      this.log('\n❌ Files that failed to fix:');
      this.errors.forEach(error => {
        this.log(`  - ${path.relative(this.projectRoot, error.file)}: ${error.error}`);
      });
    }
  }

  generateReport() {
    const report = {
      timestamp: new Date().toISOString(),
      totalFilesChecked: this.findJavaScriptFiles().length,
      fixedFiles: this.fixedFiles.length,
      failedFiles: this.errors.length,
      fixedFileList: this.fixedFiles.map(f => path.relative(this.projectRoot, f)),
      errors: this.errors
    };

    const reportPath = path.join(this.projectRoot, 'syntax-fix-report.json');
    fs.writeFileSync(reportPath, JSON.stringify(report, null, 2));
    this.log(`📄 Report saved to: ${reportPath}`);
  }
}

// Run the fixer if this file is executed directly
if (require.main === module) {
  const fixer = new ComprehensiveSyntaxFixer();
  fixer.fixAllFiles()
    .then(() => {
      fixer.generateReport();
      process.exit(0);
    })
    .catch(error => {
      console.error('Error fixing syntax:', error);
      process.exit(1);
    });
}

module.exports = ComprehensiveSyntaxFixer;