# Merge each branch in batches to avoid overwhelming the system
echo "📦 Starting batch merge process..."

# Process branches in smaller batches
batch_size=20
total_branches=${#RECENT_BRANCHES[@]}
batches=$(( (total_branches + batch_size - 1) / batch_size ))

for ((batch=0; batch<batches; batch++)); do
    start=$((batch * batch_size))
    end=$((start + batch_size))
    if [ $end -gt $total_branches ]; then
        end=$total_branches
    fi
    
    echo "🔄 Processing batch $((batch + 1))/$batches (branches $((start + 1))-$end of $total_branches)"
    
    for ((i=start; i<end; i++)); do
        branch="${RECENT_BRANCHES[$i]}"
        echo "📦 Attempting to merge $branch..."
        
        # Try to merge the branch
        if git merge "$branch" --no-edit; then
            echo "✅ Successfully merged $branch"
        else
            echo "⚠️  Merge conflict in $branch, resolving..."
            
            # Resolve conflicts by accepting our changes
            git checkout --ours .
            git add .
            git commit -m "Resolve merge conflicts in $branch - accept our changes"
            
            echo "✅ Resolved conflicts and merged $branch"
        fi
    done
    
    # Small delay between batches
    sleep 2
done

echo "🎉 All branches merged successfully!"
echo "📤 Pushing changes to main..."

# Push the merged changes
git push origin main --force

