// Function to generate page name from file path
function getPageName(filePath) {
  const fileName = path.basename(filePath, '.tsx');
  const dirName = path.basename(path.dirname(filePath));
  
  if (fileName === 'page') {
    return dirName.split('-').map(word => 
      word.charAt(0).toUpperCase() + word.slice(1)
    ).join('');
  }
  
  return fileName.split('-').map(word => 
    word.charAt(0).toUpperCase() + word.slice(1)
  ).join('');
}

// Function to generate title from page name
function getTitle(pageName) {
  return pageName.replace(/([A-Z])/g, ' $1').trim();
}

// Function to generate description
function getDescription(pageName) {
  return `Professional ${pageName.toLowerCase().replace(/([A-Z])/g, ' $1').trim()} solutions powered by advanced AI and cutting-edge technology.`;
}

// Function to generate keywords
function getKeywords(pageName) {
  const baseKeywords = 'AI solutions, IT services, automation, technology, business solutions';
  const pageKeywords = pageName.toLowerCase().replace(/([A-Z])/g, ' $1').trim();
  return `${pageKeywords}, ${baseKeywords}`;
}

// Main function to restore all pages
async function main() {
  const appDir = path.join(__dirname, 'app');
  const pattern = path.join(appDir, '**/page.tsx');
  
  const files = await glob(pattern);
  
  console.log(`Found ${files.length} page files to restore`);
  
  files.forEach(filePath => {
    try {
      const pageName = getPageName(filePath);
      const title = getTitle(pageName);
      const description = getDescription(pageName);
      const keywords = getKeywords(pageName);
      
      const content = servicePageTemplate(pageName, title, description, keywords);
      
      // Ensure directory exists
      const dir = path.dirname(filePath);
      if (!fs.existsSync(dir)) {
        fs.mkdirSync(dir, { recursive: true });
      }
      
      fs.writeFileSync(filePath, content, 'utf8');
      console.log(`Restored: ${filePath}`);
    } catch (error) {
      console.error(`Error restoring ${filePath}:`, error.message);
    }
  });
  
  console.log('Page restoration completed!');
}

main().catch(console.error);