// Performance optimization utilities for better loading and user experience

export class PerformanceOptimizer {
  private static instance: PerformanceOptimizer;
  private criticalResources: Set<string> = new Set();
  private preloadedChunks: Set<string> = new Set();

  static getInstance(): PerformanceOptimizer {
    if (!PerformanceOptimizer.instance) {
      PerformanceOptimizer.instance = new PerformanceOptimizer();
    }
    return PerformanceOptimizer.instance;
  }

  // Preload critical chunks based on user navigation patterns
  preloadCriticalChunks(): void {
    const criticalRoutes = ['/services', '/about', '/contact', '/pricing'];
    
    criticalRoutes.forEach(route => {
      this.preloadRouteChunk(route);
    });
  }

  // Preload specific route chunks
  private preloadRouteChunk(route: string): void {
    if (this.preloadedChunks.has(route)) return;

    // Create link element for preloading
    const link = document.createElement('link');
    link.rel = 'prefetch';
    
    // Map routes to their likely chunk names
    const chunkMapping: Record<string, string> = {
      '/services': 'Services',
      '/about': 'About',
      '/contact': 'Contact',
      '/pricing': 'Pricing',
    };

    const chunkName = chunkMapping[route];
    if (chunkName) {
      // This is a best guess - actual chunk names will be generated by Vite
      link.href = `/assets/${chunkName}-[hash].js`;
      document.head.appendChild(link);
      this.preloadedChunks.add(route);
    }
  }

  // Intersection Observer for lazy loading
  setupIntersectionObserver(): IntersectionObserver {
    return new IntersectionObserver(
      (entries) => {
        entries.forEach(entry => {
          if (entry.isIntersecting) {
            const element = entry.target as HTMLElement;
            const route = element.dataset.route;
            if (route) {
              this.preloadRouteChunk(route);
            }
          }
        });
      },
      { 
        rootMargin: '100px',
        threshold: 0.1 
      }
    );
  }

  // Optimize images with lazy loading
  optimizeImages(): void {
    const images = document.querySelectorAll('img[data-src]');
    
    if ('IntersectionObserver' in window) {
      const imageObserver = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
          if (entry.isIntersecting) {
            const img = entry.target as HTMLImageElement;
            img.src = img.dataset.src || '';
            img.classList.remove('lazy');
            imageObserver.unobserve(img);
          }
        });
      });

      images.forEach(img => imageObserver.observe(img));
    } else {
      // Fallback for browsers without IntersectionObserver
      images.forEach(img => {
        const imgElement = img as HTMLImageElement;
        imgElement.src = imgElement.dataset.src || '';
      });
    }
  }

  // Resource hints for better loading
  addResourceHints(): void {
    const hints = [
      { rel: 'dns-prefetch', href: '//fonts.googleapis.com' },
      { rel: 'dns-prefetch', href: '//fonts.gstatic.com' },
      { rel: 'preconnect', href: 'https://api.stripe.com' },
    ];

    hints.forEach(hint => {
      const link = document.createElement('link');
      link.rel = hint.rel;
      link.href = hint.href;
      if (hint.rel === 'preconnect') {
        link.crossOrigin = 'anonymous';
      }
      document.head.appendChild(link);
    });
  }

  // Bundle size monitoring
  monitorBundleSize(): void {
    if (process.env.NODE_ENV === 'development') {
      const observer = new PerformanceObserver((list) => {
        list.getEntries().forEach((entry) => {
          if (entry.entryType === 'resource' && entry.name.includes('.js')) {
            const resource = entry as PerformanceResourceTiming;
            const size = resource.transferSize;
            if (size > 100000) { // 100KB threshold
              // // console.warn(`Large bundle detected: ${entry.name} (${(size / 1024).toFixed(2)}KB)`);
            }
          }
        });
      });
      
      observer.observe({ entryTypes: ['resource'] });
    }
  }

  // Memory usage monitoring
  monitorMemoryUsage(): void {
    if (process.env.NODE_ENV === 'development' && 'memory' in performance) {
      setInterval(() => {
        const memory = (performance as any).memory;
        if (memory.usedJSHeapSize > 50 * 1024 * 1024) { // 50MB threshold
          // // console.warn('High memory usage detected:', {
          //   used: `${(memory.usedJSHeapSize / 1024 / 1024).toFixed(2)}MB`,
          //   total: `${(memory.totalJSHeapSize / 1024 / 1024).toFixed(2)}MB`,
          //   limit: `${(memory.jsHeapSizeLimit / 1024 / 1024).toFixed(2)}MB`
          // });
        }
      }, 30000); // Check every 30 seconds
    }
  }

  // Initialize all optimizations
  initialize(): void {
    this.preloadCriticalChunks();
    this.addResourceHints();
    this.optimizeImages();
    this.monitorBundleSize();
    this.monitorMemoryUsage();
    
    // Setup intersection observer for navigation preloading
    const observer = this.setupIntersectionObserver();
    
    // Observe navigation elements
    setTimeout(() => {
      const navElements = document.querySelectorAll('[data-route]');
      navElements.forEach(el => observer.observe(el));
    }, 1000);
  }
}

// Export singleton instance
export const performanceOptimizer = PerformanceOptimizer.getInstance();

