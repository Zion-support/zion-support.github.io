// Helper functions for filtering and searching
export const filterServicesByIndustry = (industry) => {
  return SPECIALIZED_INDUSTRY_SOLUTIONS_2025.filter(service => service.industry === industry);
};

export const filterServicesBySubcategory = (subcategory) => {
  return SPECIALIZED_INDUSTRY_SOLUTIONS_2025.filter(service => service.subcategory === subcategory);
};

export const filterServicesByPriceRange = (minPrice, maxPrice) => {
  return SPECIALIZED_INDUSTRY_SOLUTIONS_2025.filter(service => 
    service.price >= minPrice && service.price <= maxPrice
  );
};

export const searchIndustryServices = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return SPECIALIZED_INDUSTRY_SOLUTIONS_2025.filter(service => 
    service.name.toLowerCase().includes(lowercaseQuery) ||
    service.description.toLowerCase().includes(lowercaseQuery) ||
    service.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery))
  );
};

export const getIndustryServiceStats = () => {
  const totalServices = SPECIALIZED_INDUSTRY_SOLUTIONS_2025.length;
  const totalValue = SPECIALIZED_INDUSTRY_SOLUTIONS_2025.reduce((sum, service) => sum + service.price, 0);
  const averageROI = SPECIALIZED_INDUSTRY_SOLUTIONS_2025.reduce((sum, service) => {
    const roi = parseInt(service.roi.match(/\d+/)?.[0] || "0");
    return sum + roi;
  }, 0) / totalServices;
  const industries = [...new Set(SPECIALIZED_INDUSTRY_SOLUTIONS_2025.map(service => service.industry))];
  
  return {
    totalServices,
    totalValue,
    averageROI: Math.round(averageROI),
    industries,
    averagePrice: Math.round(totalValue / totalServices)
  };
};

export const getServicesByIndustry = () => {
  const industryMap = {};
  SPECIALIZED_INDUSTRY_SOLUTIONS_2025.forEach(service => {
    if (!industryMap[service.industry]) {
      industryMap[service.industry] = [];
    }
    industryMap[service.industry].push(service);
  });
  return industryMap;
};