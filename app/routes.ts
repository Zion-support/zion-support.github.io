// Helper function to get component name from path
export const getComponentName = (path: string): string => {
  const route = routes.find(r => r.path === path)
  return route ? route.component : 'NotFoundPage'
}

// Helper function to get all routes
export const getAllRoutes = () => routes

// Helper function to get routes by category
export const getRoutesByCategory = (category: string) => {
  return routes.filter(route => route.path.startsWith(`/${category}`))
}