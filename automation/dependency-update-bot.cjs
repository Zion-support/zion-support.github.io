#!/usr/bin/env node
/**
 * Dependency Update Bot — autonomous, safe, auto-merge-ready
 *
 * Runs `npm outdated --json`, filters minor/patch updates, creates isolated branches,
 * opens PRs via GitHub CLI, and optionally auto-merges when CI passes.
 *
 * Safety-first defaults: excludes majors, batches ≤20, dry-run mode, respects lockfile.
 */

import { execSync } from 'child_process';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const CONFIG = {
  dryRun: process.env.DEPENDENCY_BOT_DRY_RUN === 'true',
  allowMajor: process.env.DEPENDENCY_BOT_ALLOW_MAJOR === 'true',
  maxBatchSize: parseInt(process.env.DEPENDENCY_BOT_MAX_BATCH || '20', 10),
  ignore: (process.env.DEPENDENCY_BOT_IGNORE || '').split(',').map(s => s.trim()).filter(Boolean),
  manualReview: process.env.DEPENDENCY_BOT_MANUAL_REVIEW !== 'false', // default true
  maxPraAgeHours: parseInt(process.env.DEPENDENCY_BOT_MAX_PR_AGE_HOURS || '48', 10),
  stateDir: path.resolve(__dirname, '../.hermes/memory/dependency-bot'),
  branchPrefix: 'deps/update-',
};

function ensureStateDir() {
  if (!fs.existsSync(CONFIG.stateDir)) {
    fs.mkdirSync(CONFIG.stateDir, { recursive: true });
  }
}

function log(msg) {
  const timestamp = new Date().toISOString();
  console.log(`[${timestamp}] ${msg}`);
}

function runCommand(cmd, cwd = process.cwd()) {
  log(`Running: ${cmd}`);
  const output = execSync(cmd, { cwd, encoding: 'utf-8', stdio: ['pipe', 'pipe', 'pipe'] });
  return output;
}

function getOutdatedPackages() {
  try {
    const raw = runCommand('npm outdated --json');
    const data = JSON.parse(raw);
    const updates = [];

    for (const [name, info] of Object.entries(data)) {
      if (CONFIG.ignore.includes(name)) {
        log(`Skipping ignored package: ${name}`);
        continue;
      }

      const current = info.current;
      const latest = info.latest;
      const wanted = info.wanted;

      // Determine if update is allowed
      let allow = true;
      if (info.major !== 0 && !CONFIG.allowMajor) {
        allow = false; // major version change excluded by default
      }

      if (allow) {
        updates.push({
          name,
          current,
          wanted,
          latest,
          isMajor: info.major !== 0,
        });
      }
    }

    return updates;
  } catch (err) {
    log(`Error checking outdated packages: ${err.message}`);
    return [];
  }
}

function createBranch() {
  const date = new Date();
  const timestamp = date.toISOString().replace(/[:.]/g, '-').slice(0, 19);
  const branchName = `${CONFIG.branchPrefix}${timestamp}`;
  runCommand(`git checkout -b ${branchName}`);
  return branchName;
}

function updatePackageJson(updates) {
  const pkgPath = path.resolve(process.cwd(), 'package.json');
  const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf8'));

  let changed = false;
  for (const { name, wanted } of updates) {
    if (pkg.dependencies && pkg.dependencies[name] && pkg.dependencies[name] !== wanted) {
      pkg.dependencies[name] = wanted;
      changed = true;
      log(`Update dependency: ${name} → ${wanted}`);
    }
    if (pkg.devDependencies && pkg.devDependencies[name] && pkg.devDependencies[name] !== wanted) {
      pkg.devDependencies[name] = wanted;
      changed = true;
      log(`Update devDependency: ${name} → ${wanted}`);
    }
  }

  if (changed) {
    fs.writeFileSync(pkgPath, JSON.stringify(pkg, null, 2) + '\n');
  }
  return changed;
}

function commitChanges(updates) {
  const summary = updates.map(u => `${u.name}@${u.wanted}`).join(', ');
  runCommand('git add package.json package-lock.json');
  runCommand(`git commit -m "chore(deps): update ${summary}"`);
}

function pushAndCreatePr(branchName, updates) {
  runCommand(`git push -u origin ${branchName}`);

  const summary = updates.map(u => `- ${u.name}: ${u.current} → ${u.wanted}`).join('\n');

  // Create PR via gh CLI
  const title = `chore(deps): update ${updates.length} package(s)`;
  const body = `Automated dependency updates:\n\n${summary}\n\n---\n*Generated by Dependency Update Bot*`;
  
  runCommand(`gh pr create --title "${title}" --body "${body}" --base main --head ${branchName}`);
  
  log(`Opened PR for branch ${branchName}`);
}

function recordState(branchName, updates) {
  ensureStateDir();
  const record = {
    timestamp: new Date().toISOString(),
    branch: branchName,
    updates,
    prNumber: null, // to be filled if needed
  };
  const historyPath = path.join(CONFIG.stateDir, 'history.json');
  let history = [];
  if (fs.existsSync(historyPath)) {
    history = JSON.parse(fs.readFileSync(historyPath, 'utf8'));
  }
  history.push(record);
  fs.writeFileSync(historyPath, JSON.stringify(history, null, 2));
}

function main() {
  log('=== Dependency Update Bot starting ===');
  ensureStateDir();

  const updates = getOutdatedPackages();
  log(`Found ${updates.length} updatable packages`);

  if (updates.length === 0) {
    log('No updates needed. Exiting.');
    process.exit(0);
  }

  // Batch if too many
  const batch = updates.slice(0, CONFIG.maxBatchSize);
  if (updates.length > CONFIG.maxBatchSize) {
    log(`Truncating to first ${CONFIG.maxBatchSize} packages to keep PR reviewable`);
  }

  if (CONFIG.dryRun) {
    log('DRY RUN mode — no changes will be committed');
    console.log('Would update:', batch.map(u => `${u.name}@${u.wanted}`).join(', '));
    process.exit(0);
  }

  // Create branch
  const branchName = createBranch();
  log(`Created branch: ${branchName}`);

  // Update package.json
  const changed = updatePackageJson(batch);
  if (!changed) {
    log('No actual version changes after filtering. Aborting.');
    process.exit(0);
  }

  // Commit
  commitChanges(batch);

  // Push + PR
  pushAndCreatePr(branchName, batch);

  // Record
  recordState(branchName, batch);

  log('=== Dependency Update Bot complete ===');
}

main().catch(err => {
  console.error('Fatal error:', err);
  process.exit(1);
});
