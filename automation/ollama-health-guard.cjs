#!/usr/bin/env node
/**
 * Autonomous LLM Fallback Health Guard
 * Monitors Ollama availability and auto-restarts service if needed.
 * Also verifies the unified API endpoint responds.
 *
 * Runs via cron/GitHub Actions. On repeated failures, opens a GitHub issue.
 */

import { readFile, writeFile } from 'fs/promises';
import { exec } from 'child_process';
import { resolve } from 'path';

const STATE_FILE = 'automation/reports/ollama-health-state.json';
const LOG_FILE = 'automation/reports/ollama-health.log';

interface State {
  consecutiveFails: number;
  lastSuccess: number | null;
  lastAlert: number | null;
  lastRestart: number | null;
}

async function readState(): Promise<State> {
  try {
    const raw = await readFile(STATE_FILE, 'utf-8');
    return JSON.parse(raw);
  } catch {
    return { consecutiveFails: 0, lastSuccess: null, lastAlert: null, lastRestart: null };
  }
}

function writeState(s: State) {
  return writeFile(STATE_FILE, JSON.stringify(s, null, 2));
}

function log(msg: string) {
  const line = `[${new Date().toISOString()}] ${msg}`;
  console.log(line);
  // append to log file (fire-and-forget)
  require('fs').appendFile(LOG_FILE, line + '\n', () => {});
}

async function isOllamaUp(): Promise<boolean> {
  try {
    const res = await fetch('http://localhost:11434/api/tags', { signal: AbortSignal.timeout(3000) });
    return res.ok;
  } catch {
    return false;
  }
}

async function isAPIUp(): Promise<boolean> {
  try {
    const res = await fetch('http://localhost:3000/api/llm/chat', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ messages: [{ role: 'user', content: 'hi' }], forceLocal: true }),
      signal: AbortSignal.timeout(10000),
    });
    return res.ok;
  } catch {
    return false;
  }
}

function restartOllama(): Promise<void> {
  return new Promise((resolve, reject) => {
    exec('pkill ollama || true', (err) => {
      // ignore kill errors
      setTimeout(() => {
        exec('ollama serve > /tmp/ollama-serve.log 2>&1 &', (err2) => {
          if (err2) reject(err2);
          else resolve();
        });
      }, 1000);
    });
  });
}

function openIssue(title: string, body: string) {
  // Use GitHub CLI if available (gh), else output instructions
  exec(`gh issue create --title "${title}" --body "${body}" --label "automation/ollama-health"`, (err, stdout, stderr) => {
    if (err) {
      log(`Failed to create issue via gh: ${err.message}. Manual action required.`);
    } else {
      log(`GitHub issue created: ${stdout.trim()}`);
    }
  });
}

async function main() {
  log('=== Ollama Health Check ===');
  const state = await readState();
  const now = Date.now();

  const ollamaUp = await isOllamaUp();
  if (!ollamaUp) {
    log('❌ Ollama down. Attempting restart...');
    try {
      await restartOllama();
      await new Promise((r) => setTimeout(r, 3000)); // wait for boot
      const afterRestart = await isOllamaUp();
      if (!afterRestart) throw new Error('Still down after restart');
      log('✅ Ollama restarted successfully');
      state.consecutiveFails = 0;
      state.lastRestart = now;
    } catch (e) {
      state.consecutiveFails++;
      log(`❌ Restart failed (attempt ${state.consecutiveFails})`);
    }
  } else {
    log('✅ Ollama is up');
    state.consecutiveFails = 0;
    state.lastSuccess = now;
  }

  // API endpoint check (only if dev server presumably running)
  const apiUp = await isAPIUp();
  if (!apiUp) {
    log('⚠️  Unified LLM API not responding (dev server down?)');
  } else {
    log('✅ API endpoint healthy');
  }

  // Escalation: after 3 consecutive fails, create GitHub issue
  if (state.consecutiveFails >= 3) {
    const shouldAlert = !state.lastAlert || now - state.lastAlert > 3600_000; // hourly
    if (shouldAlert) {
      log('🚨 ALERT: Opening GitHub issue for persistent Ollama failure');
      openIssue(
        `[Automation] Ollama health check failing (${state.consecutiveFails} consecutive)`,
        `Ollama has been unresponsive for multiple checks.\n\nLast restart: ${new Date(state.lastRestart || now).toISOString()}\nConsecutive failures: ${state.consecutiveFails}\n\nAction required: verify disk/memory, and check logs at \`/tmp/ollama-serve.log\`.\n\nThis issue was auto-generated by \`automation/ollama-health-guard.cjs\`.`
      );
      state.lastAlert = now;
    }
  }

  await writeState(state);
  log('=== Check complete ===');
}

main().catch((err) => {
  log(`Fatal error: ${err.message}`);
  process.exit(1);
});
