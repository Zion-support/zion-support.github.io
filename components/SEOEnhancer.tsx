import React, { useEffect, useState } from 'react';

interface SEOData {
  title: string;
  description: string;
  keywords: string[];
  canonicalUrl: string;
  ogImage: string;
  ogType: string;
  twitterCard: string;
  structuredData?: any;
}

interface SEOEnhancerProps {
  seoData: SEOData;
  autoGenerate?: boolean;
  showDebug?: boolean;
}

export default function SEOEnhancer({ 
  seoData, 
  autoGenerate = true, 
  showDebug = false 
}: SEOEnhancerProps) {
  const [generatedData, setGeneratedData] = useState<SEOData>(seoData);
  const [seoScore, setSeoScore] = useState<number>(0);
  const [recommendations, setRecommendations] = useState<string[]>([]);

  useEffect(() => {
    if (autoGenerate) {
      generateSEOData();
    }
    
    updateDocumentMeta();
    injectStructuredData();
    optimizeImages();
    checkSEOScore();
  }, [generatedData, autoGenerate]);

  const generateSEOData = () => {
    const currentUrl = window.location.href;
    const currentPath = window.location.pathname;
    
    // Auto-generate SEO data based on current page
    const autoGeneratedData: SEOData = {
      title: generateTitle(currentPath),
      description: generateDescription(currentPath),
      keywords: generateKeywords(currentPath),
      canonicalUrl: currentUrl,
      ogImage: generateOGImage(currentPath),
      ogType: generateOGType(currentPath),
      twitterCard: 'summary_large_image',
      structuredData: generateStructuredData(currentPath)
    };
    
    setGeneratedData(autoGeneratedData);
  };

  const generateTitle = (path: string): string => {
    const titleMap: { [key: string]: string } = {
      '/': 'Zion Tech Group — AI, Micro SaaS, and IT Services',
      '/services': 'AI & IT Services — Zion Tech Group',
      '/blog': 'AI & Tech Blog — Latest Insights & Innovations',
      '/about': 'About Zion Tech Group — AI & IT Solutions',
      '/contact': 'Contact Zion Tech Group — Get Started Today'
    };
    
    return titleMap[path] || `${path.replace('/', '').replace('-', ' ')} — Zion Tech Group`;
  };

  const generateDescription = (path: string): string => {
    const descriptionMap: { [key: string]: string } = {
      '/': 'Enterprise-grade AI, micro SaaS, and IT solutions. Transform your business with cutting-edge technology and automation.',
      '/services': 'Comprehensive AI and IT services including data analytics, workflow automation, cloud migration, and cybersecurity.',
      '/blog': 'Latest AI trends, tech innovations, and practical insights to help your business stay ahead of the competition.',
      '/about': 'Learn about Zion Tech Group\'s mission to deliver cutting-edge AI and IT solutions that drive business success.',
      '/contact': 'Get in touch with our AI and IT experts. Free consultation and custom solutions for your business needs.'
    };
    
    return descriptionMap[path] || `Explore ${path.replace('/', '').replace('-', ' ')} services and solutions at Zion Tech Group.`;
  };

  const generateKeywords = (path: string): string[] => {
    const baseKeywords = ['AI services', 'IT solutions', 'micro SaaS', 'cloud automation', 'enterprise software'];
    
    const pathKeywords: { [key: string]: string[] } = {
      '/': ['AI transformation', 'business automation', 'cloud migration', 'DevOps', 'SRE'],
      '/services': ['AI analytics', 'workflow automation', 'virtual assistant', 'cloud migration', 'cybersecurity'],
      '/blog': ['AI trends', 'tech insights', 'innovation', 'digital transformation', 'AI adoption'],
      '/about': ['AI company', 'IT consulting', 'technology partner', 'digital solutions', 'AI expertise'],
      '/contact': ['AI consultation', 'IT support', 'business transformation', 'AI implementation', 'tech consulting']
    };
    
    return [...baseKeywords, ...(pathKeywords[path] || [])];
  };

  const generateOGImage = (path: string): string => {
    const baseUrl = window.location.origin;
    return `${baseUrl}/og-images/${path.replace('/', '') || 'home'}.jpg`;
  };

  const generateOGType = (path: string): string => {
    if (path === '/') return 'website';
    if (path.startsWith('/blog')) return 'article';
    return 'website';
  };

  const generateStructuredData = (path: string) => {
    const baseUrl = window.location.origin;
    
    if (path === '/') {
      return {
        '@context': 'https://schema.org',
        '@type': 'Organization',
        name: 'Zion Tech Group',
        url: baseUrl,
        logo: `${baseUrl}/logo.png`,
        description: generatedData.description,
        address: {
          '@type': 'PostalAddress',
          streetAddress: '364 E Main St STE 1008',
          addressLocality: 'Middletown',
          addressRegion: 'DE',
          postalCode: '19709',
          addressCountry: 'US'
        },
        contactPoint: {
          '@type': 'ContactPoint',
          telephone: '+1-302-464-0950',
          contactType: 'customer service',
          email: 'kleber@ziontechgroup.com'
        },
        sameAs: [
          'https://linkedin.com/company/zion-tech-group',
          'https://twitter.com/ziontechgroup'
        ]
      };
    }
    
    if (path.startsWith('/blog')) {
      return {
        '@context': 'https://schema.org',
        '@type': 'BlogPosting',
        headline: generatedData.title,
        description: generatedData.description,
        url: window.location.href,
        datePublished: new Date().toISOString(),
        author: {
          '@type': 'Organization',
          name: 'Zion Tech Group'
        },
        publisher: {
          '@type': 'Organization',
          name: 'Zion Tech Group',
          logo: {
            '@type': 'ImageObject',
            url: `${baseUrl}/logo.png`
          }
        }
      };
    }
    
    return null;
  };

  const updateDocumentMeta = () => {
    // Update title
    document.title = generatedData.title;
    
    // Update meta description
    updateMetaTag('description', generatedData.description);
    
    // Update keywords
    updateMetaTag('keywords', generatedData.keywords.join(', '));
    
    // Update canonical URL
    updateLinkTag('canonical', generatedData.canonicalUrl);
    
    // Update Open Graph tags
    updateMetaTag('og:title', generatedData.title, 'property');
    updateMetaTag('og:description', generatedData.description, 'property');
    updateMetaTag('og:url', generatedData.canonicalUrl, 'property');
    updateMetaTag('og:type', generatedData.ogType, 'property');
    updateMetaTag('og:image', generatedData.ogImage, 'property');
    
    // Update Twitter Card tags
    updateMetaTag('twitter:card', generatedData.twitterCard);
    updateMetaTag('twitter:title', generatedData.title);
    updateMetaTag('twitter:description', generatedData.description);
    updateMetaTag('twitter:image', generatedData.ogImage);
    
    // Update robots meta
    updateMetaTag('robots', 'index, follow');
    
    // Update viewport meta
    updateMetaTag('viewport', 'width=device-width, initial-scale=1.0');
  };

  const updateMetaTag = (name: string, content: string, attribute: string = 'name') => {
    let meta = document.querySelector(`meta[${attribute}="${name}"]`);
    
    if (!meta) {
      meta = document.createElement('meta');
      meta.setAttribute(attribute, name);
      document.head.appendChild(meta);
    }
    
    meta.setAttribute('content', content);
  };

  const updateLinkTag = (rel: string, href: string) => {
    let link = document.querySelector(`link[rel="${rel}"]`);
    
    if (!link) {
      link = document.createElement('link');
      link.setAttribute('rel', rel);
      document.head.appendChild(link);
    }
    
    link.setAttribute('href', href);
  };

  const injectStructuredData = () => {
    if (generatedData.structuredData) {
      // Remove existing structured data
      const existingScript = document.querySelector('script[type="application/ld+json"]');
      if (existingScript) {
        existingScript.remove();
      }
      
      // Add new structured data
      const script = document.createElement('script');
      script.type = 'application/ld+json';
      script.textContent = JSON.stringify(generatedData.structuredData);
      document.head.appendChild(script);
    }
  };

  const optimizeImages = () => {
    const images = document.querySelectorAll('img');
    images.forEach(img => {
      // Add loading="lazy" for performance
      if (!img.hasAttribute('loading')) {
        img.setAttribute('loading', 'lazy');
      }
      
      // Add alt text if missing
      if (!img.hasAttribute('alt')) {
        img.setAttribute('alt', generateAltText(img.src));
      }
      
      // Add width and height for CLS prevention
      if (!img.hasAttribute('width') || !img.hasAttribute('height')) {
        img.addEventListener('load', () => {
          img.setAttribute('width', img.naturalWidth.toString());
          img.setAttribute('height', img.naturalHeight.toString());
        });
      }
    });
  };

  const generateAltText = (src: string): string => {
    const filename = src.split('/').pop()?.split('.')[0] || '';
    return filename.replace(/[-_]/g, ' ').replace(/\b\w/g, l => l.toUpperCase());
  };

  const checkSEOScore = () => {
    let score = 100;
    const newRecommendations: string[] = [];
    
    // Check title length
    if (generatedData.title.length < 30 || generatedData.title.length > 60) {
      score -= 10;
      newRecommendations.push('Title should be 30-60 characters long');
    }
    
    // Check description length
    if (generatedData.description.length < 120 || generatedData.description.length > 160) {
      score -= 10;
      newRecommendations.push('Description should be 120-160 characters long');
    }
    
    // Check keywords count
    if (generatedData.keywords.length < 5) {
      score -= 5;
      newRecommendations.push('Add more relevant keywords (5-10 recommended)');
    }
    
    // Check for images without alt text
    const imagesWithoutAlt = document.querySelectorAll('img:not([alt])');
    if (imagesWithoutAlt.length > 0) {
      score -= 15;
      newRecommendations.push(`Add alt text to ${imagesWithoutAlt.length} images`);
    }
    
    // Check for heading structure
    const h1Count = document.querySelectorAll('h1').length;
    if (h1Count !== 1) {
      score -= 10;
      newRecommendations.push('Use exactly one H1 tag per page');
    }
    
    // Check for internal links
    const internalLinks = document.querySelectorAll('a[href^="/"], a[href*="' + window.location.hostname + '"]');
    if (internalLinks.length < 3) {
      score -= 5;
      newRecommendations.push('Add more internal links to improve site structure');
    }
    
    setSeoScore(Math.max(0, score));
    setRecommendations(newRecommendations);
  };

  if (!showDebug) {
    return null;
  }

  return (
    <div className="fixed bottom-4 left-4 bg-white rounded-lg shadow-lg p-4 max-w-sm border border-gray-200">
      <div className="flex items-center justify-between mb-3">
        <h3 className="text-lg font-semibold text-gray-900">SEO Score</h3>
        <span className={`text-xl font-bold ${
          seoScore >= 90 ? 'text-green-600' :
          seoScore >= 70 ? 'text-yellow-600' :
          'text-red-600'
        }`}>
          {seoScore}
        </span>
      </div>
      
      <div className="space-y-2 mb-4">
        <div className="text-sm">
          <span className="text-gray-600">Title:</span>
          <span className="ml-2 font-medium">
            {generatedData.title.length} chars
          </span>
        </div>
        <div className="text-sm">
          <span className="text-gray-600">Description:</span>
          <span className="ml-2 font-medium">
            {generatedData.description.length} chars
          </span>
        </div>
        <div className="text-sm">
          <span className="text-gray-600">Keywords:</span>
          <span className="ml-2 font-medium">
            {generatedData.keywords.length}
          </span>
        </div>
      </div>
      
      {recommendations.length > 0 && (
        <div>
          <h4 className="text-sm font-semibold text-gray-900 mb-2">Recommendations:</h4>
          <ul className="space-y-1">
            {recommendations.map((rec, index) => (
              <li key={index} className="text-xs text-gray-600">
                • {rec}
              </li>
            ))}
          </ul>
        </div>
      )}
      
      <button
        onClick={checkSEOScore}
        className="mt-3 text-xs text-blue-600 hover:text-blue-800 underline"
      >
        Refresh Score
      </button>
    </div>
  );
}

// Hook for using SEO data
export function useSEOData(initialData: SEOData) {
  const [seoData, setSeoData] = useState<SEOData>(initialData);
  
  const updateSEOData = (updates: Partial<SEOData>) => {
    setSeoData(prev => ({ ...prev, ...updates }));
  };
  
  return { seoData, updateSEOData };
}