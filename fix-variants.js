// Get all TypeScript files in the current directory
const files = fs.readdirSync('.')
  .filter(file => file.endsWith('.ts') && !file.includes('node_modules'))
  .filter(file => !file.includes('types.ts') && !file.includes('service-variants.ts'));

console.log(`Found ${files.length} TypeScript files to process`);

let totalReplacements = 0;

files.forEach(file => {
  let content = fs.readFileSync(file, 'utf8');
  let fileReplacements = 0;
  
  // Replace all invalid variants
  Object.entries(variantMapping).forEach(([invalid, valid]) => {
    const regex = new RegExp(`variant: '${invalid.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}'`, 'g');
    const matches = content.match(regex);
    if (matches) {
      content = content.replace(regex, `variant: '${valid}'`);
      fileReplacements += matches.length;
      console.log(`  ${file}: Replaced ${matches.length} instances of '${invalid}' with '${valid}'`);
    }
  });
  
  if (fileReplacements > 0) {
    fs.writeFileSync(file, content);
    totalReplacements += fileReplacements;
  }
});

console.log(`\nTotal replacements made: ${totalReplacements}`);