#!/usr/bin/env bash
set -e
cd $(git rev-parse --show-toplevel)
# Run npm audit in JSON mode
echo "Running npm audit..."
npm audit --json > audit.json || true
VULNS=$(jq -r '.metadata.vulnerabilities.total // 0' audit.json)
if [ "$VULNS" -eq 0 ]; then
  echo "✅ No vulnerabilities found"
  exit 0
fi
echo "⚠️ Found $VULNS vulnerabilities – attempting safe auto‑fix"
# Try safe fixes (patch/minor only)
npm audit fix --audit-level=moderate || true
# Check if lockfile changed
if git diff --quiet package-lock.json; then
  echo "⚠️ Auto‑fix did not modify lockfile – manual review required"
  # Create issue with details
  gh issue create -t "Manual dependency fix required" -b "npm audit reported $VULNS issues. Auto‑fix could not resolve them. See audit.json for details."
else
  # Create a branch and PR
  BRANCH="deps-fix-$(date +%s)"
  git checkout -B $BRANCH
  git add package.json package-lock.json audit.json
  git commit -m "fix: auto‑resolve dependency vulnerabilities"
  git push -f origin $BRANCH
  gh pr create -B main -t "fix: auto‑resolve dependency vulnerabilities" -b "Automated PR generated by dependency_upgrader.sh"
  echo "✅ PR created for dependency upgrades"
fi
